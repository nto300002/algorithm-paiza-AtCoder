"""
行数 H , 列数 W の盤面があり、各マスには文字が 1 つだけ書かれています。盤面と y , x 座標 が与えられるので、盤面の与えられた座標の文字が "." の場合は "#" に、"#" の場合は "." に書き換えた後の盤面を出力してください。

なお、マスの座標系は左上端のマスの座標を ( y , x ) = ( 0 , 0 ) とし、 下方向が y 座標の正の向き、右方向が x 座標の正の向きとします。
"""

"""
ステップバイステップで解決方法を考えます：

計画：

入力値を受け取る (H, W, 盤面情報, y, x座標)
盤面を2次元リストとして保存
指定された座標の文字を反転
結果を出力
実装詳細：

盤面は文字列のリストとして扱う
座標(y,x)の文字を確認し、"." なら "#" に、"#" なら "." に変更
変更後の盤面を1行ずつ出力
"""

H, W = map(int, input().split())
grid = []

#入力を受け取る
for _ in range(H):
    grid.append(list(input()))
y, x = map(int, input().split())

#指定された座標の文字を反転
if grid[y][x] == '.':
    grid[y][x] = '#'
else:
    grid[y][x] = '.'

#結果を出力
for row in grid:
    print(''.join(row))
